#CICLOS while
i = 1 
while i < 32:
    print(i)
    i+= 1

else:
    print("i no es menor que 32")

#ciclo for 
sports = ["football", "tennis", "basketball", "baseball"]

for sp in sports:
    print(sp)

#con rango 
for n in range(4, 12):
    print(n)

# con rango de 3 en 3
for n in range(1, 20, 3):
    print(n)